						<p class="text-5"><span class="font-weight-extra-bold d-spacing">Рассмотрим некоторые из навыков, которыми необходимо обладать директору, чтобы выполнять свои непростые управленческие функции.</span></p>
						<p class="text-5">• Организовывать свою жизнь. Для директора это критический навык, поскольку его личная эффективность должна быть выше, чем остальных сотрудников. Это значит не просто работать в компании, это значит, что любые проблемы: кризис, пожар в офисе, сотрудники поувольнялись, сборная вылетела из чемпионата - не важны. Директор - это тот, кто умеет оставаться эффективным несмотря ни на что.</p><br>
						<p class="text-5">• Ключевой навык директора – достигать больших результатов при малых ресурсах и в стрессовых обстоятельствах.</p><br>
						
                        <p class="text-5">• Планировать свое время. При множестве текущих задач, постоянных “пожарах”, стрессах и отсутствии профессиональных управленцев в подчинении, директору необходима четкая структура, на которую можно было бы опереться в течение дня, недели, месяца. Достичь этого можно только с помощью грамотного планирования.</p><br>
                        <p class="text-5">• Переключаться с работы на отдых и обратно. Для работы в стрессовых условиях высшие управленцы должны быть способны находить время на отдых и восстановление сил.</p><br>
                        <p class="text-5">• Находить оптимальные решения в существующих условиях, глядя на существующую компанию, видеть огромное количество возможностей. Это о том, что можно и как сделать, чтобы исправить положение, исходя из того, что мы имеем здесь и сейчас.</p><br>
                        <p class="text-5">• Принимать правильные и взвешенные решения. Находить истинные данные и принимать решения на основе фактов, а не мнений и интерпретаций других людей. Директор принимает много качественных решений каждый день.</p><br>
                        <p class="text-5">• Самостоятельность и автономность. Директору не нужно ничего для того, чтобы выполнять свои функции. Для того, чтобы управлять компанией, ему не нужно вообще ничего, он может организовать работу даже в пустыне.</p><br>
                        <p class="text-5">• Делегировать. Делегировать на уровне направлений, функций и процессов. Достигать максимального результата через делегирование, подбирать оптимальных исполнителей и оптимальное сочетание ролей для достижения целей.</p><br>
                        <p class="text-5">• Держать свое эмоциональное состояние под контролем и уметь транслировать собственное эмоциональное состояние коллективу. Это про умение заряжать энергией сотрудников независимо от того, есть ли у него эта энергия или нет.</p><br>
                        <p class="text-5">• Мотивировать людей на достижение цели. Директор умеет мотивировать, объяснять людям, почему им самим важно работать хорошо.</p><br>
                        <p class="text-5">• Разрабатывать мотивационные схемы для топов и руководителей, чтобы продуктивные люди приходили работать в компанию, получали достойное вознаграждение и добивались огромных результатов.</p><br>
                        <p class="text-5">• Эффективно работать в стрессовых ситуациях. Это когда стресс не является препятствием для эффективной работы. Умение собираться, концентрироваться и не проваливаться эмоционально, не обращать внимания на эмоции, принимать факты, объективно обрабатывать информацию и принимать решения.</p><br>
                        <p class="text-5">• Разбираться в людях. Он должен обладать чуйкой в отношении людей: кто они, что из себя представляют, кто будет разрушителем в компании, а кто будет создавать и реализовывать цели. Если директор не умеет разбираться в людях, то это может привести к краху компанию. В своей практике я очень часто вижу, как ставят на пост директора людей, которые очень красиво и умело вешают 10-ти метровую лапшу на уши Владельца, а у Владельца нет такой вилки, чтобы от нее избавиться. После того, как поставили такого директора на пост, мы видим, что он занимается чем угодно и находится в состоянии "справляться", но не "организовывает" бизнес-процессы и людей. Более того, по отношению к некоторым сотрудникам он может быть в задабривании или что еще хуже - в неспособности отстоять свою точку зрения, и принимать от них оправдания, почему нельзя что-то сделать или прийти к какому-то результату. Так что гоните в шею того, кто не умеет разбираться в людях и отстаивать свою точку зрения, направленную на достижение результатов компании, перед сотрудниками.</p><br>
                        <p class="text-5">• Одно из самых важных, но часто упускаемых качеств - это обучаемость и стремление к постоянному повышению своей квалификации как управленца. Постоянно учиться, постоянно развиваться. И он не просто учится-развивается, он совершенствует навыки. Директор – это тот, кто четко понимает реальную пользу от учения. Директор сам вкладывает деньги в свое обучение, поскольку он тут же конвертирует эти знания в увеличение дохода. Из любого обучающего продукта он умеет вытаскивать пользу, знания, навыки. Он постоянно должен знать, что происходит на передовой его отрасли.</p><br>
                        <p class="text-5">• Быть наставником и коучем для своих топов и руководителей. Он постоянно мотивирует подчиненных на обучение и повышение квалификации, поддерживает в достижении результатов, делится успешными действиями.</p><br>
                        
