						<p class="text-5"><span class="d-caps">B</span> любом бизнесе сотрудники для меня делятся на 2 категории: есть работники, а есть сотрудники.</p>
						
						<p class="text-5">
								<span class="font-weight-extra-bold d-spacing">Кто такой работник?</span><br>
								Давайте для начала взглянем на значение слова “работник” -  это человек, работающий в какой-нибудь сфере, тот, кто работает, трудится. И происходит от слов «работа, рабство».</p>
						<p class="text-5">Итак, работник - это человек, который продает свое время за деньги, не труд или результат, а именно время. Он не сильно ориентирован на результат и работает от звонка до звонка. Это люди, которые приходят более менее вовремя на работу, и уходят они точно вовремя, если в 7 часов вечера заканчивается работа, то в 7 часов он уже закрывает за собой дверь. Работник не сильно мотивирован целью компании, он не переживает за то, будет осуществляться миссия компании или нет, он в основном нацелен на выполнение механической работы. </p><br>
						<p class="text-5">К сожалению, таких людей более 60%, по моим наблюдениям. Такие люди нужны, но за ними нужен жесткий контроль и дисциплина. Они любят жесткую и высокую загруженность. Им нужен линейный менеджер, бригадир, который будет их постоянно загружать работой и который будет спрашивать с них результат. Это четкая постановка задач и жесткий контроль результата. Как правило, в больших компаниях это очень хорошо отлажено. Четко прописаны инструкции, человека ставят, человека обучают работать, он механически работает и производит результаты. Ждать от них высокой производительности не стоит, но они должны выполнять механическую работу. </p><br>
						<p class="text-5">
								<span class="font-weight-extra-bold d-spacing">А сотрудники - это кто?</span><br>
							
								Давайте тоже посмотрим на значение слова “сотрудник” - это человек, который работает вместе с кем-нибудь, является помощником. С моей точки зрения, если разложить это слово (со + труд + ник), то получится, что “сотрудник” это тот человек, который вместе с кем-то осуществляет целесообразную деятельность, работу, требующую умственного и физического напряжения для достижения результата.</p><br>
						<p class="text-5">Сотрудник - это человек, который уже разделяет цели компании, и он нацелен на производство результата, то есть его интересуют полученные результаты. И он уже может остаться после работы, если результата нет. Это тоже может быть человек, который выполняет механическую работу, но у него другой мотив - он не может допустить плохой работы, ему важно, чтобы была выполнена работа и получен результат.</p><br>
						<blockquote class="blockquote-primary m-5">Для него важно, чтобы качество того, что он сделал, соответствовало требованиям.</blockquote>
						<p class="text-5">Он находится в сотрудничестве не только с директором и владельцем компании, он находится в сотрудничестве и с другими людьми. И если ему передают что-то недоделанное, он стремится либо это доделать, либо вернуть, чтобы это доделали. Для него важно, чтобы качество того, что он сделал, соответствовало требованиям. Это люди, более вовлеченные в работу компании, их интересует то, что компания меняет в мире, как выглядят клиенты. Сотрудник постоянно старается что-то улучшить в компании. Он всячески старается помочь руководителям. Это тот человек, который, когда свободен, поднимает руку и говорит: “Есть еще что-то, чем я могу помочь?”</p><br>
						<p class="text-5">
								<span class="font-weight-extra-bold d-spacing">Сотрудников можно разделить на следующие статусы снизу вверх:</span><br><br>

								<span class="font-italic">Работник</span><br><br>
								<span class="font-italic">Специалист</span>
								- это человек, который является профессионалом в своей области. Как правило, это вредный человек, который всячески стремится выполнить свою работу очень хорошо, но не позволит оскорблять его и его достоинство. Этот человек всегда направлен на свой профессиональный рост. Он не стремится к руководящему посту, но всячески стремится совершенствоваться.</p><br>
								<p class="text-5"><span class="font-italic">Руководитель проекта</span> (может быть и специалист) - это сотрудник, которому поручают задание выполнить что-то, и он в состоянии разложить это на более мелкие процессы и выполнить процессы последовательно для получения результата. У этих людей не всегда могут быть в подчинении люди, но они знают, как взаимодействовать для того, чтобы выполнить какое-то задание, которое состоит не только из того, чтобы сделать одну вещь, но и связать со многими другими.</p><br>
								
						<p class="text-5"><span class="font-italic">Линейный менеджер</span> - это тот, у кого в подчинении до 10, иногда до 20 человек. Он в состоянии описать проект и некоторые процессы, которые может раздать определенным людям и проконтролировать их выполнение. Этот человек более мотивирован, он больше отвечает за результат, лучше понимает свою причинность и несет ответственность за свои решения.</p><br>
						<p class="text-5"> <span class="font-italic">Начальник</span> - это человек, который уже несет ответственность за целую область. Он хорошо умеет делегировать задачи и процессы, и добиваться результата.</p><br>
						<p class="text-5"><span class="font-italic">Руководитель среднего звена/уровня</span> - это человек, который уже в состоянии несколько функций объединить в одну, разложить это в бизнес-процессы, согласовать действия не только с работниками и сотрудниками, но и с другими руководителями разных уровней. Он в состоянии проводить координации и отмечать какие-то глобальные вещи, отвечать за бОльшие области. </p><br>
						<p class="text-5"><span class="font-italic">ТОП-менеджер</span> - это те люди, которые способны превращать планы в жизнь. Они планируют на полгода вперед, понимают как организовать эту деятельность, как нанять руководителя, какими они должны быть и какими качествами должны обладать. ТОП-менеджеры отвечают за большую область и с этой позиции являются хорошими помощниками для директоров, и вносят значительный вклад в компанию и ее развитие.
							<br><br><span class="font-italic">Директор</span><br><br>
							<span class="font-italic">Председатель совета директоров (в некоторых компаниях)</span><br><br>
							<span class="font-italic">Владелец/Основатель</span>
								
								
								</p><br>
						<p class="text-5">Какой бы статус у сотрудника ни был, в определенный момент времени каждый из них может играть какую-то роль в компании. Они играют эту роль по отношению к конкретной игре, в конкретной компании. И он в этой игре исполняет свою роль - руководитель, сотрудник, работник, но здесь есть нюанс, который относится к его настроению и поведению в этой компании… отвечающий на вопрос - как я отношусь к тому, что я делаю?</p><br>
						<p class="text-5">Итак, это может быть поведение:<br>
								- нытика, "жертвы", "офисного планктона", “плесени”<br>
								- середнячка<br>
								- бриллианта</p><br>
						<p class="text-5"><span class="font-italic">“Нытик”, “жертва”, “офисный планктон”, “плесень”</span> - так я могу охарактеризовать
								таких людей (да простят меня сотрудники). Такой человек пришел в компанию не для реализации целей компании, он не заинтересован вообще, чтобы компания развивалась и разрасталась, он заинтересован только в получении своей выгоды. Его выгода не всегда в деньгах, иногда это просто потешить свое самолюбие. Но, чаще всего, у него не получается это хорошо, он не может быть звездой, которая пользуется уважением и хорошим расположением других людей. Он может "звездеть" (болтать, говорить впустую). </p><br>
								<blockquote class="blockquote-primary m-5">И словно плесень поражают все вокруг. Если затянуть время его работы в компании, то убирая потом таких людей из компании, очень часто приходится много времени тратить на лечение других.</blockquote>
						<p class="text-5">Он создает к себе внимание посредством манипуляций: он может ныть, болеть, играть бедного и несчастного. Такие люди очень часто находятся в настроении лицемерия, задабривания, или даже жертвы… От них нельзя ждать добра. Они разрушают область, в которой работают, компанию, людей, с которыми взаимодействуют. Они еще те актеры. И если честно, то мне их жалко, они вечно чем-то недовольны. И словно плесень поражают все вокруг. Если затянуть время его работы в компании, то убирая потом таких людей из компании, очень часто приходится много времени тратить на лечение других в его отделе или с кем он взаимодействовал, а то даже и зачищать зараженную область. Но самое интересное - этот человек может быть в компании таким, а дома может быть другим. </p><br>
						<p class="text-5">Почему вообще такие люди есть? Все потому, что деятельность конкретной компании, в которой они играют эту роль, не соответствует их целям и стремлению к реализации. Поэтому, если он перейдет в другую компанию, где больше может достичь поставленных перед собою целей, то он может подняться… НО, такое бывает очень редко, по крайней мере в моем опыте.</p><br>
						<p class="text-5"><span class="font-italic">“Середнячок”</span>- это ни рыба ни мясо… это тот, кто не берет на себя инициативу, просто наблюдает и поддерживает тех, кого ему выгодно поддерживать. Он тоже на собственной выгоде работает в компании. Это человек, который пришел в компанию, чтобы реализовывать свои цели, но из-за отсутствия хорошего управления у него появилось некое недовольство тем, как к нему относятся, и он из позиции активного, когда он предлагал свое участие - “я этим помогу, вот это сделаю”,  перешел на позицию середнячка. Чаще всего это связано с тем, что его пресекали: “Хватит! Не лезь! Отстань! Мы без тебя справимся!”. </p><br>
						<p class="text-5">Это внутренняя обида, невозможность отстоять свою точку зрения и желание обвинять других. Очень часто эти люди ищут поддержки в окружении против тех, кто их обидел. И бывает такое, что они идут к плесени чаще, чем к нормальным, эффективным сотрудникам, так как плесень чаще с ними общается. Все происходит потому, что середнячок ослабил свой иммунитет и плесень, видя возможность быстрого заражения, очень грамотно и умело обхаживает середнячка, причем делает это искренне, переживая, сочувствуя ему и с огромным желанием и рвением ему помочь, на что и попадается середнячок. Здесь есть еще одна скрытая сторона - “бриллианты” настолько увлечены производством и решением проблем, что им нет дела до середнячков и плесени, в связи с чем последние объединяются и поддерживают друг друга, и глобальная проблема состоит в том, что руководители не замечают этой ситуации, и чаще всего обращают внимание на плесень и середнячков, чтобы те не воняли и не заражали всю компанию, а помалкивали. Но это не решает проблему, а только усугубляет. Так как есть закон “чему даем подтверждение и внимание, то и получаем”. Но середнячка не трудно перевести в статус бриллианта, главное вытащить все обиды и несогласия и убрать от плесени.</p><br>
						<p class="text-5"><span class="font-italic">“Бриллиант”</span> - это тот человек, у которого “шило в попе”, в хорошем смысле. Он видит свой потенциал, видит свое развитие, замечает различные неоптимальности, которые на его взгляд можно улучшить. Он старается играть в компании, создавая прогресс. Плюс ко всему, он очень любит создавать продукты и результаты. Его от этого прет, штырит, колбасит, он радуется. И, даже если получает ошибку, то говорит - “отлично, сейчас сделаю примочку и пойду дальше”. Он постоянно развивается, личностно и профессионально, он не всегда получает награды, но действует из позиции “я самореализуюсь”. Даже если он получает нагоняй от руководства, он все равно чувствует удовлетворение, что он реализуется. Это люди, которые заняты производством продукта, а не те, кто создает видимость работы. Они в интересе по отношению к компании, к окружению, они с радостью берутся за решение тех или иных задач. Им нет времени жаловаться, высказывать свое недовольство и уделять внимание нытикам.</p><br>
						<p class="text-5">Для высшего руководителя из них троих, если посмотреть, каждый может создать проблемы, но бриллианты пытаются найти решение, и, если они все-таки идут с проблемой к руководству, это означает, что они большую часть возможных решений рассмотрели, и им нужно от руководства только правильное направление. Но наиболее существенные проблемы, остановки в развитии бизнеса создает плесень, нытики. Эти люди, как бы мы не хотели, собирают зрителей в виде середнячков, которые на них посмотрят и, возможно, пожалеют их или хотя бы посочувствуют.</p><br>
						<p class="text-5">Только в компаниях, где есть хорошая мотивация, где есть руководитель, который обучен мотивировать людей и смотреть за тем, как люди работают - там середнячков меньшинство, а большинство бриллиантов.</p><br>

