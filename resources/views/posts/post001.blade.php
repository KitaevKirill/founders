						<p class="text-5"><span class="d-caps">К</span> нам часто обращаются основатели компаний, чтобы мы помогли наладить бизнес-процессы в их
							проектах. И я понимаю, насколько для них это актуальный вопрос. Владельцы компаний устают от хаоса в бизнесе,
							борьбы с сотрудниками, которые сопротивляются нововведениям, бесконечного решения каких-то проблем.</p>
						<p class="text-5">Но знаете, что самое удручающее? Когда я общаюсь с Владельцами, то наблюдаю следующее: потухшие
							глаза, апатию по отношению к своему бизнесу, какую-то растерянность в самом человеке, которую сложно описать
							словами… Я ее просто чувствую. Это выглядит, словно человек потерялся в лесу, у него нет карты, и он из
							последних уже сил бредет, сам не зная куда. </p>
						<p class="text-5">В таком состоянии у человека одно стремление — сбежать из беспросветной чащобы проблем и
							спрятаться на острове. Желательно, почти необитаемом. При таком настрое уже и речи нет о том, чтобы развивать
							бизнес, проявлять энтузиазм в работе и в жизни. На это нет желания, не осталось внутренних сил.</p><br>
						<p class="text-5">Но выход из дебрей проблем и хандры есть.
							Я знаю, как помочь владельцам компаний снова обрести драйв для новых свершений, пробудить желания, ярко увидеть
							дальнейший путь. Весь секрет в том, что невозможно привести в порядок бизнес, пока сам основатель и его мысли в
							таком замешательстве. Я наглядно это увидела в работе. Мой опыт в цифрах — более 1000 людей и 200 компаний.</p>
						<blockquote class="blockquote-primary m-5">Проблема заключается в том, что Владелец забыл, зачем ему этот бизнес и
								ради чего он так усердствует, забывая про себя… </blockquote>
							<p class="text-5">С моей точки зрения, проблема заключается в том, что Владелец забыл, зачем ему этот бизнес и
									ради чего он так усердствует, забывая про себя… Ведь на самом деле глаза горели, они горели в самом начале,
									когда человек принял решение открыть бизнес, именно свое дело. В этот момент времени у него не было мысли.</p>
						<p class="text-5"><span class="font-weight-extra-bold d-spacing">А что потом?!</span><br> Потом начались
							первые контракты, найм сотрудников, увеличение компании и позиций на рынке. Но, как обычно бывает, сотрудники
							справляются не со всеми проблемами, клиенты создают какие-то ситуации, и жизнь уже не сказка… И Владелец из
							чувства долга продолжает заниматься компанией, но без всякого удовольствия! Он устал! Жизненная энергия его
							покидает и хочется либо сбежать на острова, либо продать бизнес… Он потерялся!</p>
						<p class="text-5"><span class="font-weight-extra-bold d-spacing">А все почему?!</span><br> В самом начале
							создания компании человек стремился к какой-то определенной цели. Возьмем, к примеру, решение вопросов с доходом
							и известность, свободу. Вот он достиг дохода, о котором мечтал, определенной известности и свободы. А новую цель
							себе не поставил. Сотрудники обычно думают, что цель Владельца - зарабатывать деньги. Владелец же, чаще всего,
							не озвучивает свою цель сотрудникам. И получается, что компания и команда не знают, куда идут, а если и знают,
							то не более чем на год - они не понимают, ради чего им это все и насколько счастливы будут они сами.</p>
						<p class="text-5">Обычно, из моих наблюдений, цель деятельности должна отвечать <span class="font-weight-extra-bold">4
								критериям:</span><br><br>
							<span class="font-weight-extra-bold"><span class="d-caps ml-5 mr-2">1</span> </span>Зарабатывать деньги	 - именно так. Любая
							деятельность должна приносить доход, мы же не занимаемся благотворительностью в жизни. Очень часто в малом и
							среднем бизнесе сталкиваемся с тем, что Владельцу на регулярной основе не переводятся дивиденды, или они по
							штатному расписанию получают меньше денег за свой труд, чем некоторые ТОПы, а то и вовсе бездельники, пожирающие
							время компании и других сотрудников. И это проблема. Награда за создание бизнеса должна быть! Должна быть
							награда за организацию работы для сотрудников, да и вообще за то, что эта компания существует.<br><br>
							<span class="font-weight-extra-bold"><span class="d-caps ml-5 mr-2">2</span></span> Получать удовольствие - то есть то, чем мы
							занимаемся, должно приносить удовольствие, а именно: новые знакомства, благодарности, подтверждения, и так
							далее. Если мы не получаем подтверждения нашему труду, и вокруг нас негатив, то надолго нас не хватит.
							Удовольствие - это также и свобода путешествий, время с семьей.<br><br>
							<span class="font-weight-extra-bold"><span class="d-caps ml-5 mr-2">3</span> </span>Иметь точки роста - мы должны расти в этой
							области, развиваться. Познавать что-то новое. Обучение, знакомства, связи, новые рынки и так далее.<br><br>
							<span class="font-weight-extra-bold"><span class="d-caps ml-5 mr-2">4</span></span> Помогать людям - и все-таки отчасти
							благотворительность нужна, но именно в том, чтобы помогать людям. Например, помогать клиентам решать свои
							вопросы в вашей специфике деятельности, поставщикам - иметь отличного партнера, сотрудникам - иметь потрясающую
							работу и рабочие места, свой рост и развитие, и так далее.</p>
						<p class="text-5">Эти критерии относятся не только к Владельцам, но и к каждому сотруднику. Может ли сотрудник
							быть нам полезен, если он несчастлив и ваша компания вообще не его цель и не мечта, или он не хочет обучаться?</p>
						<p class="text-5">Но это не основное! Основное - это ВЫ! Когда Вы
							последний раз мечтали? Мечтали по-крупному и ставили перед собой большие цели? </p>
						<p class="text-5">Я думаю, что давненько! И все, что нужно сделать - это взять лист бумаги и просто помечтать.
							Напишите свои материальные мечты, минимум 40. Не оценивая их, не преуменьшая их. Напишите их просто, а потом
							выберите из них 5 легко достижимых и начните осуществлять. Наблюдайте за собой, что происходит! Жизнь заиграет
							красками, и станет намного легче управлять компанией и помогать людям получать удовольствие от того, чем они
							занимаются!</p>
                        
